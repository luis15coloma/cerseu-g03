"""Conversión de datos"""

var1 = 2023
var2 = str(var1)

print("El valor de la variable var2 es: {}".format(var2))
print("El tipo de dato de la variable var2 es: {}".format(type(var2)))

"""Conversión de int a float"""

var3 = 1998
var4 = float(var3)

print("El valor de la variable var4 es: {}".format(var4))
print("El tipo de dato de la variable var4 es: {}".format(type(var4)))